#include "lib.h"

/**
 * add - adds two integers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: sum of two integers
 */ 

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subracts two numbers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: result of subraction of two numbers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: mul of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two numbers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: division of to numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two numbers
 * @a: The first operand
 * @b: The second operand
 *
 * Return: modulus of two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
