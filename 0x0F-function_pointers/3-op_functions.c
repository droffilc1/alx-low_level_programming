#include <stdio.h>

/**
 * op_add - sum of two nums
 * @a: The first operand
 * @b: The second operand
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- subtracts of two nums
 * @a: The first operand
 * @b: The second operand
 *
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two nums
 * @a: The first operand
 * @b: The second operand
 *
 * Return: result of mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides of two nums
 * @a: The first operand
 * @b: The second operand
 *
 * Return: result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modm of two nums
 * @a: The first operand
 * @b: The second operand
 *
 * Return: result of mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
